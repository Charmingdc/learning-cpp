#include <stdio.h>

int main() {
  printf("An integer: %d\n", 18);
  printf("A large integer: %ld\n", 1838484849338);
  printf("A float: %f\n", 18.0);
  printf("A large float: %lf\n", 18.37383748383838);
  printf("A character: %c\n", 'm');
  printf("A string: %s\n,", "Learning about inputs placeholders in C");
}